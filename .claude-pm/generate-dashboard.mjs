import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

const directory = path.dirname(fileURLToPath(import.meta.url));
const inputPath = path.join(directory, "WORK_ITEMS.json");
const outputPath = path.join(directory, "dashboard-data.js");
const checkOnly = process.argv.includes("--check");
const allowed = {
  type: new Set(["feature", "bug", "chore", "planning", "research", "documentation"]),
  status: new Set(["backlog", "ready", "in_progress", "blocked", "done", "cancelled"]),
  priority: new Set(["critical", "high", "medium", "low"])
};
const fail = message => { throw new Error(`WORK_ITEMS.json: ${message}`); };
const requireString = (value, label) => {
  if (typeof value !== "string" || value.trim() === "") fail(`${label} must be a non-empty string`);
};
const requireStringArray = (value, label) => {
  if (!Array.isArray(value) || value.some(entry => typeof entry !== "string" || entry.trim() === "")) fail(`${label} must be an array of non-empty strings`);
};

function validate(data) {
  if (data.schemaVersion !== 1) fail("schemaVersion must equal 1");
  requireString(data.updatedAt, "updatedAt");
  if (!data.project || typeof data.project !== "object") fail("project must be an object");
  requireString(data.project.name, "project.name");
  requireStringArray(data.project.authoritativeSources, "project.authoritativeSources");
  if (!Array.isArray(data.items)) fail("items must be an array");
  const ids = new Set();
  for (const [index, item] of data.items.entries()) {
    const label = `items[${index}]`;
    if (!item || typeof item !== "object") fail(`${label} must be an object`);
    requireString(item.id, `${label}.id`);
    if (!/^WI-\d{4}$/.test(item.id)) fail(`${label}.id must match WI-0000`);
    if (ids.has(item.id)) fail(`${label}.id duplicates ${item.id}`);
    ids.add(item.id);
    for (const field of ["title", "owner", "summary", "createdAt", "updatedAt"]) requireString(item[field], `${label}.${field}`);
    for (const field of ["type", "status", "priority"]) if (!allowed[field].has(item[field])) fail(`${label}.${field} has unsupported value ${item[field]}`);
    for (const field of ["acceptanceCriteria", "dependencies", "sourceRefs", "evidence", "notes"]) requireStringArray(item[field], `${label}.${field}`);
    if (item.status === "done" && item.evidence.length === 0) fail(`${label} is done but has no evidence`);
  }
  const graph = new Map(data.items.map(item => [item.id, item.dependencies]));
  for (const item of data.items) for (const dependency of item.dependencies) {
    if (!ids.has(dependency)) fail(`${item.id} references missing dependency ${dependency}`);
    if (dependency === item.id) fail(`${item.id} depends on itself`);
  }
  const visiting = new Set(), visited = new Set();
  const visit = id => {
    if (visiting.has(id)) fail(`dependency cycle includes ${id}`);
    if (visited.has(id)) return;
    visiting.add(id);
    for (const dependency of graph.get(id)) visit(dependency);
    visiting.delete(id); visited.add(id);
  };
  for (const id of ids) visit(id);
}

let data;
try { data = JSON.parse(await readFile(inputPath, "utf8")); }
catch (error) { fail(`invalid JSON (${error.message})`); }
validate(data);
const output = `// Generated by generate-dashboard.mjs. Do not edit.\nwindow.CLAUDE_PM_DATA = ${JSON.stringify(data, null, 2)};\n`;
if (checkOnly) {
  let existing = "";
  try { existing = await readFile(outputPath, "utf8"); }
  catch { fail("dashboard-data.js is missing; run npm run pm:dashboard"); }
  if (existing !== output) fail("dashboard-data.js is stale; run npm run pm:dashboard");
  console.log(`Validated ${data.items.length} work items; dashboard data is current.`);
} else {
  await writeFile(outputPath, output, "utf8");
  console.log(`Validated ${data.items.length} work items and generated ${path.relative(process.cwd(), outputPath)}.`);
}
